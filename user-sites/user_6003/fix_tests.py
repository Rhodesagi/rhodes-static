import re

replacements = [
    # (old_pattern, new_bytes)
    (r'bytes\(\[0x97, 0x12, 0x34, 0x52\]\)', 'bytes([0x97, 0x50, 0x34, 0x18])'),  # AUIPC
    (r'bytes\(\[0xef, 0x05, 0x00, 0x01\]\)', 'bytes([0xef, 0x00, 0x00, 0x01])'),  # JAL
    (r'bytes\(\[0x67, 0x08, 0x18, 0x08\]\)', 'bytes([0x67, 0x00, 0x81, 0x00])'),  # JALR
    (r'bytes\(\[0xe3, 0x08, 0x21, 0x00\]\)', 'bytes([0x63, 0x88, 0x20, 0x00])'),  # BEQ
    (r'bytes\(\[0x23, 0x20, 0x21, 0x02\]\)', 'bytes([0x23, 0xa0, 0x20, 0x00])'),  # SW
    (r"emu\.load_program\(bytes\(\[0x83, 0x21, 0x01, 0x00\]\), addr=4\)", 'emu.load_program(bytes([0x83, 0xa1, 0x00, 0x00]), addr=4)'),  # LW
    (r'bytes\(\[0xb3, 0x01, 0x21, 0x00\]\)', 'bytes([0xb3, 0x81, 0x20, 0x00])'),  # ADD
    (r'bytes\(\[0x33, 0x02, 0x11, 0x40\]\)', 'bytes([0x33, 0x02, 0x11, 0x40])'),  # SUB (correct)
    (r'bytes\(\[0xf3, 0x02, 0x21, 0x00\]\)', 'bytes([0xf3, 0x02, 0x21, 0x00])'),  # AND
    (r'bytes\(\[0x33, 0x03, 0x21, 0x00\]\)', 'bytes([0x33, 0x03, 0x21, 0x00])'),  # OR
    (r'bytes\(\[0x33, 0x43, 0x21, 0x00\]\)', 'bytes([0x33, 0x43, 0x21, 0x00])'),  # XOR
    (r'bytes\(\[0x13, 0x09, 0x11, 0x01\]\)', 'bytes([0x13, 0x09, 0x11, 0x01])'),  # SLLI
    (r'bytes\(\[0x13, 0x09, 0x21, 0x00\]\)', 'bytes([0x13, 0x09, 0x21, 0x00])'),  # SRLI
    (r'bytes\(\[0x13, 0x4d, 0x04, 0x40\]\)', 'bytes([0x13, 0x4d, 0x04, 0x40])'),  # SRAI
    (r'bytes\(\[0x33, 0x22, 0x01, 0x00\]\)', 'bytes([0x33, 0x22, 0x01, 0x00])'),  # SLT
    (r'bytes\(\[0xb3, 0x22, 0x00, 0x00\]\)', 'bytes([0xb3, 0x22, 0x00, 0x00])'),  # SLT (2nd)
    (r'bytes\(\[0x33, 0x33, 0x01, 0x00\]\)', 'bytes([0x33, 0x33, 0x01, 0x00])'),  # SLTU
]

with open('riscv_emulator.py', 'r') as f:
    content = f.read()

for old, new in replacements:
    content = re.sub(old, new, content)

with open('riscv_emulator.py', 'w') as f:
    f.write(content)

print("Fixed test encodings")
